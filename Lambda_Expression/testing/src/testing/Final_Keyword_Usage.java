package testing;

public class Final_Keyword_Usage {
	public static void main(String args[])
	{
		final int a;
		System.out.println(a);
		
		System.out.println("Hello");
		
		/*Usage of final
		 * 1) Final variable cannot be changed.
		 * 2) final method cannot be override.
		 * 3) final method can be inherited.
		 * 4) if final variable is not initialised then it is called "Blank Final Variable"
		 * 5) exact usage of final is by using constructor
		 * 	like example
		 * final int PanCard;
		 * it can be changed by constructor(int a....,int PanCard)
		 * {
		 * this.PanCard = PanCard;
		 * ...
		 * }
		 * 
		 */
		
	}

}
